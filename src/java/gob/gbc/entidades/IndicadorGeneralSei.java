package gob.gbc.entidades;

/**
 *
 * @author jarguelles
 */
public class IndicadorGeneralSei {

    private String claveIndicador;
    private String nombreIndicador;
    private String periodicidad;
    private String objetivoIndicador;
    private String ramo;
    private String ramoDescr;
    private String unidadMedida;
    private String prog;
    private String progDescr;
    private String tipoPresentacion;
    private String inicio;
    private String fechaIni;
    private String year;
    private String dimensionId;
    
    public IndicadorGeneralSei() {
    }

    public String getClaveIndicador() {
        return claveIndicador;
    }

    public void setClaveIndicador(String claveIndicador) {
        this.claveIndicador = claveIndicador;
    }

    public String getNombreIndicador() {
        return nombreIndicador;
    }

    public void setNombreIndicador(String nombreIndicador) {
        this.nombreIndicador = nombreIndicador;
    }

    public String getPeriodicidad() {
        return periodicidad;
    }

    public void setPeriodicidad(String periodicidad) {
        this.periodicidad = periodicidad;
    }

    public String getObjetivoIndicador() {
        return objetivoIndicador;
    }

    public void setObjetivoIndicador(String objetivoIndicador) {
        this.objetivoIndicador = objetivoIndicador;
    }

    public String getRamo() {
        return ramo;
    }

    public void setRamo(String ramo) {
        this.ramo = ramo;
    }

    public String getRamoDescr() {
        return ramoDescr;
    }

    public void setRamoDescr(String ramoDescr) {
        this.ramoDescr = ramoDescr;
    }

    public String getUnidadMedida() {
        return unidadMedida;
    }

    public void setUnidadMedida(String unidadMedida) {
        this.unidadMedida = unidadMedida;
    }

    public String getProg() {
        return prog;
    }

    public void setProg(String prog) {
        this.prog = prog;
    }

    public String getProgDescr() {
        return progDescr;
    }

    public void setProgDescr(String progDescr) {
        this.progDescr = progDescr;
    }

    public String getTipoPresentacion() {
        return tipoPresentacion;
    }

    public void setTipoPresentacion(String tipoPresentacion) {
        this.tipoPresentacion = tipoPresentacion;
    }

    public String getInicio() {
        return inicio;
    }

    public void setInicio(String inicio) {
        this.inicio = inicio;
    }

    public String getFechaIni() {
        return fechaIni;
    }

    public void setFechaIni(String fechaIni) {
        this.fechaIni = fechaIni;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getDimensionId() {
        return dimensionId;
    }

    public void setDimensionId(String dimensionId) {
        this.dimensionId = dimensionId;
    }
  

}
