package gob.gbc.entidades;

/**
 *
 * @author jarguelles
 */
public class CodProg {

  private String ramo;
  private String prg;
  private String depto;
  private String area;
  private int   year;
  private String refrendo;
  private int   centroCosto;
  private int   finalidad;
  private int   funcion;
  private int   subfuncion;

    public CodProg() {
    }

    public String getRamo() {
        return ramo;
    }

    public void setRamo(String ramo) {
        this.ramo = ramo;
    }

    public String getPrg() {
        return prg;
    }

    public void setPrg(String prg) {
        this.prg = prg;
    }

    public String getDepto() {
        return depto;
    }

    public void setDepto(String depto) {
        this.depto = depto;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public String getRefrendo() {
        return refrendo;
    }

    public void setRefrendo(String refrendo) {
        this.refrendo = refrendo;
    }

    public int getCentroCosto() {
        return centroCosto;
    }

    public void setCentroCosto(int centroCosto) {
        this.centroCosto = centroCosto;
    }

    public int getFinalidad() {
        return finalidad;
    }

    public void setFinalidad(int finalidad) {
        this.finalidad = finalidad;
    }

    public int getFuncion() {
        return funcion;
    }

    public void setFuncion(int funcion) {
        this.funcion = funcion;
    }

    public int getSubfuncion() {
        return subfuncion;
    }

    public void setSubfuncion(int subfuncion) {
        this.subfuncion = subfuncion;
    }

  
    
    
    
}
