package gob.gbc.entidades;

/**
 *
 * @author ugarcia
 */
public class FuenteRecurso {
    private String fuente;
    private String fondo;
    private String recurso;
    private String fuenteRecurso;
    private String fuenteRecursoDescr;

    public String getFuente() {
        return fuente;
    }

    public void setFuente(String fuente) {
        this.fuente = fuente;
    }

    public String getFondo() {
        return fondo;
    }

    public void setFondo(String fondo) {
        this.fondo = fondo;
    }

    public String getRecurso() {
        return recurso;
    }

    public void setRecurso(String recurso) {
        this.recurso = recurso;
    }

    public String getFuenteRecurso() {
        return fuenteRecurso;
    }

    public void setFuenteRecurso(String fuenteRecurso) {
        this.fuenteRecurso = fuenteRecurso;
    }

    public String getFuenteRecursoDescr() {
        return fuenteRecursoDescr;
    }

    public void setFuenteRecursoDescr(String fuenteRecursoDescr) {
        this.fuenteRecursoDescr = fuenteRecursoDescr;
    }
    
    
}
