package gob.gbc.entidades;

/**
 *
 * @author rharo
 */
public class ProcesoMomento {
    private int clave;
    private String descr;
    private String tipo;
    private int procesoMomento;

    public int getClave() {
        return clave;
    }

    public void setClave(int clave) {
        this.clave = clave;
    }

    public String getDescr() {
        return descr;
    }

    public void setDescr(String descr) {
        this.descr = descr;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public int getProcesoMomento() {
        return procesoMomento;
    }

    public void setProcesoMomento(int procesoMomento) {
        this.procesoMomento = procesoMomento;
    }
    
    
}
