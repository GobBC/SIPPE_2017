public class AvancePoaMetaObservaciones {
     private int Meta;  
     private String ramo; 
     private int year;
     private String Observa;        
     private String observa_anual;
     private int perido;

    public int getMeta() {
        return Meta;
    }

    public String getRamo() {
        return ramo;
    }

    public int getYear() {
        return year;
    }

    public String getObserva() {
        return Observa;
    }

    public String getObserva_anual() {
        return observa_anual;
    }

    public int getPerido() {
        return perido;
    }

    public void setMeta(int Meta) {
        this.Meta = Meta;
    }

    public void setRamo(String ramo) {
        this.ramo = ramo;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public void setObserva(String Observa) {
        this.Observa = Observa;
    }

    public void setObserva_anual(String observa_anual) {
        this.observa_anual = observa_anual;
    }

    public void setPerido(int perido) {
        this.perido = perido;
    }
             
             
             
             
             
    
}
